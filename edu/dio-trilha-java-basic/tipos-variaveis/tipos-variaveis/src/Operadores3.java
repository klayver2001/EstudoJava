public class Operadores3 {
    
    public static void main(String[] args) {
        
        int numero = 5;
        // X repeticao

        // numero = numero + 2;

        numero ++; 

        System.out.println(numero); // Quando incrementado após o valor, retorna na proxima execução ;

        // Quando quiser imprimir o valor incrementado anteriormente utilize o prefixo ( antes do valor );

        System.out.println(++ numero);

        // também funciona com o decremento ;

        System.out.println(numero -- );

        System.out.println(numero);


    }








}
